package teamnova.myapplication;


public class Data {
    int image;
    String title;
    String artist;
    boolean hart;
    int hartcount;
    boolean now_play;
    int sound;
    boolean alpha;

    Data(int image, String title, String artist, boolean hart, int hartcount, boolean now_play, int sound, boolean alpha){
        this.image = image;
        this.title = title;
        this.artist = artist;
        this.hart = hart;
        this.hartcount = hartcount;
        this.sound = sound;
        this.now_play = now_play;
        this.alpha = alpha;


    }



}
